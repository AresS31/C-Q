		<h3>About Quizzes & Courses</h3>
		<p>Quizzes & Courses is a website that delivers various quizzes and courses which could be very useful 
		for every kind of people. We seek to introduce people a new way to learn, indeed through courses people 
		would be able to assimilate knowledge and then test them via quizzes. Also every registered user can suggest 
		their own quizzes or courses if they want to share it with their friends or others.</p><br>
		<p>Based in Toulouse, Quizzes & Courses was founded in June 2013 by talented students web developers/designers. 
		A team that ensures a high quality, comprehensiveness, and usefulness in each quizzes or course published.</p><br><br>
		
